    talent = talent_obj.talent if talent_obj else "–ù–µ—Ç —Ç–∞–ª–∞–Ω—Ç–æ–≤"

    # –°–æ—Ü–∏–∞–ª—å–Ω—ã–π —Å—Ç–∞—Ç—É—Å
