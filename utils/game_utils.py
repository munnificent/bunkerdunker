def generate_unique_room_code():
    import string
    session = Session()
    existing_codes = {code[0] for code in session.query(Room.code).all()}
    while True:
        code = ''.join(random.choices(string.ascii_uppercase + string.digits, k=6))
        if code not in existing_codes:
            session.close()
            return code

def generate_characteristics():
    session = Session()
    # –ü—Ä–æ—Ñ–µ—Å—Å–∏—è
    profession_obj = session.query(Profession).order_by(func.random()).first()
    profession = profession_obj.name if profession_obj else "–ë–µ–∑—Ä–∞–±–æ—Ç–Ω—ã–π"

    # –ë–∏–æ–ª–æ–≥–∏—è
    biology_obj = session.query(Biology).order_by(func.random()).first()
    if biology_obj:
        biology = f"{biology_obj.gender}, {biology_obj.age} –ª–µ—Ç, {biology_obj.body_features}"
    else:
        biology = "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ"

    # –ó–¥–æ—Ä–æ–≤—å–µ
    health_obj = session.query(Health).order_by(func.random()).first()
    health = health_obj.condition if health_obj else "–ó–¥–æ—Ä–æ–≤"

    # –•–æ–±–±–∏
    hobby_obj = session.query(Hobby).order_by(func.random()).first()
    hobby = hobby_obj.hobby if hobby_obj else "–ù–µ—Ç —Ö–æ–±–±–∏"

    # –ë–∞–≥–∞–∂
    luggage_obj = session.query(Luggage).order_by(func.random()).first()
    luggage = luggage_obj.item if luggage_obj else "–ü—É—Å—Ç–æ"

    # –§–∞–∫—Ç
    fact_obj = session.query(Fact).order_by(func.random()).first()
    fact = fact_obj.fact if fact_obj else "–ù–µ—Ç —Ñ–∞–∫—Ç–æ–≤"

    # –§–æ–±–∏—è
    phobia_obj = session.query(Phobia).order_by(func.random()).first()
    phobia = phobia_obj.phobia if phobia_obj else "–ù–µ—Ç —Ñ–æ–±–∏–π"

    # –¢–∞–ª–∞–Ω—Ç
